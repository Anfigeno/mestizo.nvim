atlantic_darker.load = function()
  if vim.version().minor < 8 then
    vim.notify_once("gruvbox.nvim: you must use neovim 0.8 or higher")
    return
  end

  -- reset colors
  if vim.g.colors_name then
    vim.cmd.hi("clear")
  end
  vim.g.colors_name = "atlantic-darker"
  vim.o.termguicolors = true
end
-- add highlights
for key, val in pairs(atlantic_darker.groups) do
  vim.api.nvim_set_hl(0, key, val)
end
-- qualcosa di simile per caricare i colori

return atlantic_darker
